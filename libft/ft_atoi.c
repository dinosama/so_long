#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		stock;

	i = 0;
	neg = 1;
	stock = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (neg == -1)
			stock = (stock * 10) - (str[i] - 48);
		else
			stock = (stock * 10) + (str[i] - 48);
		i++;
	}
	return (stock);
}
